	public static PuiVariableValues getByName(String name) {
		if (StringUtils.isEmpty(name)) {
			return null;
		}

		for (PuiVariableValues pvv : values()) {
			if (pvv.name().equals(name)) {
				return pvv;
			}
		}

		return null;
	}

