package es.prodevelop.pui9.filter;

/**
 * Filter group operation enumeration. Values are case sensitive. This operation
 * is applied to the whole group and rules of the group
 * 
 * @author Marc Gil - mgil@prodevelop.es
 */
public enum FilterGroupOperation {

	/**
	 * 'and' operation
	 */
	and,

	/**
	 * 'or' operation
	 */
	or;

}